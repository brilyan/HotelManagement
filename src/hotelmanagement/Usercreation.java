    private void TampilData(){
        int c;
        
        try{
            prepare=connection.prepareStatement("select * from user");
            resultset=prepare.executeQuery();
            ResultSetMetaData metadata = resultset.getMetaData();
            c=metadata.getColumnCount();
            
            datalist=(DefaultTableModel)tbluser.getModel();
            datalist.setRowCount(0);
            while(resultset.next()){
                Vector vector = new Vector();
                
                for(int i = 1; i<=c; i++){
                    vector.add(resultset.getString(1));
                    vector.add(resultset.getString(2));
                    vector.add(resultset.getString(3));
                }
                datalist.addRow(vector);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Gagal tampil "+e.getMessage());
        }
        
    }
    private void Ubah(){
        String name=txtname.getText();
        String username=txtusername.getText();
        String password=txtpassword.getText();
        
        try{
            prepare=connection.prepareStatement("update user set username=?, password=? where nama=?");
            prepare.setString(1,username);
            prepare.setString(2,password);
            prepare.setString(3, name);
            prepare.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Diubah!");
            TampilData();
            txtname.setText("");
            txtusername.setText("");
            txtpassword.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Data gagal diubah "+e.getMessage());
        }
    }
    
    private void Hapus(){
        String nama=txtname.getText();
        
        try{
            prepare=connection.prepareStatement("delete from user where nama=?");
            prepare.setString(1, nama);
            prepare.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Data Berhasil Dihapus!");
            TampilData();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Data Gagal Dihapus! "+e.getMessage());
        }
    }
    private void MouseClicked(){
        datalist=(DefaultTableModel)tbluser.getModel();
        int selectedindex=tbluser.getSelectedRow();
        
        txtname.setText(datalist.getValueAt(selectedindex,1).toString());
        txtusername.setText(datalist.getValueAt(selectedindex,2).toString());
        
        btntambah.setEnabled(false);
    }
